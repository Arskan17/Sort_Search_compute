def sayHi(name):
    """
    ## Says Hi

    Args:
        `name`(String): The name to say hi to.

    Returns:
        `String`: "Hey there {name}!"
    """

    return f"Hi there {name}!"

def addTwoNums(a,b):

    """
    ## Adds two numbers

    Args:
        `a`(numeral): The first number.
        `b`(numeral): The second number.

    Returns:
        `numeral`: The resulting numeral from adding `a` and `b`
    """

    return int(a+b)